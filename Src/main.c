// power-up interrupt EXT3 at PA3
void HAL_GPIO_EXTI_Callback (uint16_t GPIO_Pin)
{
	UNUSED(GPIO_Pin); // avoid warnings for interrupt pin
	EXTI->IMR &= 0xFFFFFFF7; // disable external interrupt
	EXTI->PR = 0x8; //clear interrupt flag
	interruptStartTime = HAL_GetTick();
}



// ADC conversion complete interrupt
void HAL_ADC_ConvCpltCallback (ADC_HandleTypeDef* hadc)
{
	if (hadc->Instance == ADC2) // ADC2
	{
		checkMeasurement(&hadc2, 1); // check measurement 1
		HAL_ADC_Stop(&hadc2); // stop ADC2
		adc1Busy = false;
	}
	else if (hadc->Instance == ADC3) // ADC3
	{
		checkMeasurement(&hadc3, 2); // check measurement 2
		HAL_ADC_Stop(&hadc3); // stop ADC3
		adc2Busy = false;
	}
	else // ADC1 (calibration)
	{
		adcCalibration(&hadc1);
		HAL_ADC_Stop(&hadc1); // stop ADC1
	}
}



// timer complete interrupt (every second)
void HAL_TIM_PeriodElapsedCallback (TIM_HandleTypeDef *htim)
{
	HAL_ADC_Start_IT(&hadc1); // ADC calibration
	sleepCheck(); // sleep timer
	if (powerOutputEnableCheck()) // if power enabled
		startADCs(false); // launch ADCs to measure, no power up delay
}



// launch ADCs to measure
void startADCs (_Bool powerUpDelay)
{
	if (powerUpDelay) // needs power up delay
		HAL_Delay(powerUpDelayTime);
	HAL_ADC_Start_IT(&hadc2); // start ADC2 in interrupt mode
	HAL_ADC_Start_IT(&hadc3); // start ADC3 in interrupt mode
	adc1Busy = true;
	adc2Busy = true;
}



// interrupt debounce timeout
void extInt3TimeOut (void)
{
	if (((HAL_GetTick() - interruptStartTime) > interruptDebounceTime) && (!(EXTI->IMR & 0x8))) // interrupt debounce time
	{
		if ((_Bool)HAL_GPIO_ReadPin(powerInPort, powerInPin) && (!powerIn)) // rising edge PA3
		{
			powerIn = true; // rising edge PA3
			powerIsOnSet(true);
		}
		else if (!(_Bool)HAL_GPIO_ReadPin(powerInPort, powerInPin) && (powerIn)) // falling edge PA3
		{
			powerIn = false;
			powerIsOnSet(false);
		}
		EXTI->PR = 0x8; //clear interrupt flag
		EXTI->IMR |= 0x8; // enable interrupt
	}
}


